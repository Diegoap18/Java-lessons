public class Main {
    public static void main(String[] args) {

        /*
            Operadores Relacionais:
            Operadores de igualdade (==, !=),
            so pode retornar valores booleanos, verdadeiro ou falso

            Operadores Relacionais  (>,>=,<,<=)
         */

        int numero1 = 5;
        int numero2 = 10;
        System.out.println(numero1 == numero2); //true or false
        System.out.println(numero1 != numero2); //true or false
        System.out.println(numero1 > numero2); //true or false
        System.out.println(numero1 >= numero2); //true or false
        System.out.println(numero1 < numero2); //true or false
        System.out.println(numero1 <= numero2); //true or false
        

    }
}